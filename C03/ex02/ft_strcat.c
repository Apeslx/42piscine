char	*ft_strcat(char *dest, char *src)
{
	int	c;
	int	c2;

	c = 0;
	c2 = 0;
	while (dest[c] != '\0')
	{
		c++;
	}
	while (src[c2] != '\0')
	{
		dest[c] = src[c2];
		c++;
		c2++;
	}
	return (dest);
}
